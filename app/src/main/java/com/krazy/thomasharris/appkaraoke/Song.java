public class Song {
    String id;
    String songname;
    String songname2;
    String lyric;
    String lyric2;
    String artist;
    String artist2;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSongname() {
        return songname;
    }

    public void setSongname(String songname) {
        this.songname = songname;
    }

    public String getSongname2() {
        return songname2;
    }

    public void setSongname2(String songname2) {
        this.songname2 = songname2;
    }

    public String getLyric() {
        return lyric;
    }

    public void setLyric(String lyric) {
        this.lyric = lyric;
    }

    public String getLyric2() {
        return lyric2;
    }

    public void setLyric2(String lyric2) {
        this.lyric2 = lyric2;
    }

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getArtist2() {
        return artist2;
    }

    public void setArtist2(String artist2) {
        this.artist2 = artist2;
    }
}
